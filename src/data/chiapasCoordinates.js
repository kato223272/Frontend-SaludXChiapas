// Función robusta para buscar coordenadas
export const obtenerCoords = (municipio) => {
  if (!municipio) return [16.75, -93.11]; // Default Tuxtla
  
  // 1. Busqueda Exacta
  if (COORDENADAS_CHIAPAS[municipio]) return COORDENADAS_CHIAPAS[municipio];

  // 2. Busqueda insensible a mayúsculas/acentos
  const cleanName = municipio.toLowerCase().normalize("NFD").replace(/[\u0300-\u036f]/g, "");
  const foundKey = Object.keys(COORDENADAS_CHIAPAS).find(key => 
      key.toLowerCase().normalize("NFD").replace(/[\u0300-\u036f]/g, "") === cleanName
  );

  return foundKey ? COORDENADAS_CHIAPAS[foundKey] : [16.75, -93.11]; 
}